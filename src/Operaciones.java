public class Operaciones {
	public float sumar(float n1, float n2) {
		return n1 + n2;
	}

	public float restar(float n1, float n2) {
		return n1 - n2;
	}

	public float multiplicar(float n1, float n2) {
		
		float result=n1*n2;
		return result;
	}

	public float dividir(float n1, float n2) {
		return n1 / n2;
	}
}